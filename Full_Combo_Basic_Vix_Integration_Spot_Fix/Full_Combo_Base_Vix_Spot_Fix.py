    def log_trade_event(self, event_type, details):
        """Universal logging method with GUI monitor 2 compatibility"""
        try:
            sanitized = self._sanitize_types(details)
            json_str = json.dumps(sanitized, cls=CustomJSONEncoder, ensure_ascii=False, allow_nan=False)
            # Format: timestamp - level - event_type - json_data
            # This matches the exact format expected by GUI monitor 2
            logger = logging.getLogger()
            for handler in logger.handlers:
                if isinstance(handler, logging.FileHandler):
                    # Write directly to ensure exact format
                    timestamp = datetime.now(IST).strftime('%Y-%m-%d %H:%M:%S,%f')[:-3]
                    log_line = f"{timestamp} - INFO - {event_type.upper()} - {json_str}\n"
                    handler.stream.write(log_line)
                    handler.stream.flush()
            # Also log to console normally
            print(f"[{datetime.now(IST).strftime('%H:%M:%S')}] {event_type.upper()}: {json_str}")
        except Exception as e:
            logging.error(f"Error logging trade event: {str(e)}")
            logging.info(f"{event_type.upper()} - {str(details)}")

    def _sanitize_types(self, data):
        """Recursively convert non-native types to serializable formats"""
        if isinstance(data, dict):
            return {k: self._sanitize_types(v) for k, v in data.items()}
        if isinstance(data, list):
            return [self._sanitize_types(item) for item in data]
        if isinstance(data, (Timestamp, datetime)):
            return data.isoformat()
        if isinstance(data, float):
            return round(data, 4)
        if hasattr(data, 'dtype'):  # Handle numpy types
            return data.item()
        return data

    def save_state(self):
        """Save current trading state with atomic write, retry logic, and rate-limited error logging"""
        global last_state_error_log_time, state_save_error_count

        max_retries = 3
        retry_delay = 0.05  # 50ms initial delay

        for attempt in range(max_retries):
            try:
                state = {
                    'trade_counter': self.trade_counter,
                    'open_trades': {},
                    'timestamp': datetime.now(IST).isoformat(),
                    'fixed_atm_levels': FIXED_ATM_LEVELS,
                    'active_strike_pairs': ACTIVE_STRIKE_PAIRS
                }

                for trade_id, trade in self.open_trades.items():
                    state['open_trades'][trade_id] = {
                        'trade_id': trade_id,
                        'trade_counter': trade['trade_counter'],  # Critical for matching entry/exit orders
                        'level_name': trade['level_name'],
                        'indicator_key': trade['indicator_key'],
                        'entry_time': trade['entry_time'].isoformat(),
                        'ce_strike': trade['ce_strike'],
                        'pe_strike': trade['pe_strike'],
                        'entry_spot': trade['entry_spot'],
                        'ce_ltp': trade['ce_ltp'],
                        'pe_ltp': trade['pe_ltp'],
                        'ce_token': trade['ce_token'],
                        'pe_token': trade['pe_token'],
                        'signal_info': trade.get('signal_info', {}),
                        'current_info': trade.get('current_info', {}),
                        'entry_orders_written': trade.get('entry_orders_written', False),  #  Prevent duplicate orders
                        'exit_orders_written': trade.get('exit_orders_written', False)     #  Prevent duplicate orders
                    }

                # Atomic write to prevent corruption
                state_path = self.get_state_path()
                temp_path = state_path + '.tmp'

                with open(temp_path, 'w') as f:
                    json.dump(state, f, cls=CustomJSONEncoder, indent=2)
                    f.flush()
                    # os.fsync(f.fileno())  # DISABLED: Too slow, not critical for state saves

                # Atomic rename
                if os.name == 'nt' and os.path.exists(state_path):
                    os.remove(state_path)
                os.rename(temp_path, state_path)

                # Success - reset error counter
                if state_save_error_count > 0:
                    logging.info(f" State save recovered after {state_save_error_count} failures")
                    state_save_error_count = 0
                return  # Success!

            except (PermissionError, OSError) as e:
                # Retry on file locking errors
                if attempt < max_retries - 1:
                    time_mod.sleep(retry_delay * (2 ** attempt))  # Exponential backoff
                    continue
                else:
                    # Final attempt failed - log error (but rate-limited)
                    state_save_error_count += 1
                    current_time = time_mod.time()

                    # Only log every 60 seconds to prevent log spam
                    if current_time - last_state_error_log_time > 60:
                        logging.error(f"State save failed after {max_retries} retries: {e} (failed {state_save_error_count} times)")
                        last_state_error_log_time = current_time

                    # Clean up temp file if it exists
                    temp_path = self.get_state_path() + '.tmp'
                    if os.path.exists(temp_path):
                        try:
                            os.remove(temp_path)
                        except:
                            pass

    def load_state(self):
        """Load trading state with retry logic for file locking issues"""
        state_file = self.get_state_path()
        if not os.path.exists(state_file):
            return

        max_retries = 5
        retry_delay = 0.1  # 100ms initial delay

        for attempt in range(max_retries):
            try:
                with open(state_file, 'r') as f:
                    state = json.load(f)

                saved_date = datetime.fromisoformat(state['timestamp']).date()
                if saved_date != datetime.today().date():
                    return

                self.trade_counter = state.get('trade_counter', 0)

                # Restore open trades
                for trade_id, trade_data in state.get('open_trades', {}).items():
                    self.open_trades[trade_id] = {
                        'trade_id': trade_id,
                        'trade_counter': trade_data.get('trade_counter', 0),  #  Restore counter for exit orders
                        'level_name': trade_data['level_name'],
                        'indicator_key': trade_data['indicator_key'],
                        'entry_time': datetime.fromisoformat(trade_data['entry_time']),
                        'ce_strike': trade_data['ce_strike'],
                        'pe_strike': trade_data['pe_strike'],
                        'entry_spot': trade_data['entry_spot'],
                        'ce_ltp': trade_data['ce_ltp'],
                        'pe_ltp': trade_data['pe_ltp'],
                        'ce_token': trade_data['ce_token'],
                        'pe_token': trade_data['pe_token'],
                        'signal_info': trade_data.get('signal_info', {}),
                        'current_info': trade_data.get('current_info', {}),
                        'exit_details': None,
                        'entry_orders_written': trade_data.get('entry_orders_written', False),  #  Restore flag
                        'exit_orders_written': trade_data.get('exit_orders_written', False)     #  Restore flag
                    }

                logging.info(f"Loaded {len(self.open_trades)} open trades")
                return  # Success!

            except (PermissionError, OSError, json.JSONDecodeError) as e:
                # Retry on file locking or corruption errors
                if attempt < max_retries - 1:
                    logging.warning(f"State load attempt {attempt+1}/{max_retries} failed: {e}. Retrying...")
                    time_mod.sleep(retry_delay * (2 ** attempt))  # Exponential backoff
                    continue
                else:
                    # Final attempt failed
                    logging.error(f"Failed to load state after {max_retries} attempts: {e}. Starting fresh.")

            except Exception as e:
                # Unknown error - don't retry
                logging.error(f"Unexpected error loading state: {e}. Starting fresh.")
                return

