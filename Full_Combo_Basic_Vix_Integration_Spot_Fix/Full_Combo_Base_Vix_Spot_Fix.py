def save_candles(volume_cache=None):
    """Save candles with volume cache using atomic write with retry logic and rate-limited error logging"""
    global last_candle_error_log_time, candle_save_error_count

    max_retries = 3
    retry_delay = 0.05  # 50ms initial delay

    with candle_lock:
        for attempt in range(max_retries):
            try:
                pickle_path = get_date_dir()
                temp_path = pickle_path + '.tmp'

                # Create data structure with volume cache
                if volume_cache is not None:
                    data_to_save = {
                        'candles': strike_pair_candles,
                        '_volume_cache_': volume_cache
                    }
                else:
                    # Backward compatible: save just candles if no cache provided
                    data_to_save = strike_pair_candles

                # Write to temporary file first
                with open(temp_path, 'wb') as f:
                    pickle.dump(data_to_save, f)
                    f.flush()  # Ensure data is written to disk
                    # os.fsync(f.fileno())  # DISABLED: Too slow (8-10 seconds), blocks data thread

                # Atomic rename (this is instantaneous)
                if os.name == 'nt':  # Windows
                    if os.path.exists(pickle_path):
                        os.remove(pickle_path)  # Windows requires explicit removal
                os.rename(temp_path, pickle_path)

                # Success - reset error counter
                if candle_save_error_count > 0:
                    logging.info(f" Candle save recovered after {candle_save_error_count} failures")
                    candle_save_error_count = 0
                return  # Success!

            except (PermissionError, OSError) as e:
                # Retry on file locking errors
                if attempt < max_retries - 1:
                    time_mod.sleep(retry_delay * (2 ** attempt))  # Exponential backoff
                    continue
                else:
                    # Final attempt failed - log error (but rate-limited)
                    candle_save_error_count += 1
                    current_time = time_mod.time()

                    # Only log every 60 seconds to prevent log spam
                    if current_time - last_candle_error_log_time > 60:
                        logging.error(f"Error saving candle data after {max_retries} retries: {e} (failed {candle_save_error_count} times)")
                        last_candle_error_log_time = current_time

                    # Clean up temp file if it exists
                    temp_path = get_date_dir() + '.tmp'
                    if os.path.exists(temp_path):
                        try:
                            os.remove(temp_path)
                        except:
                            pass
            except Exception as e:
                # Other unexpected errors
                logging.error(f"Unexpected error saving candle data: {e}")
                return

def update_candles_by_indicator_key(strike_pair_data, fetcher):
    """
    Update candles using IndicatorKey instead of strangle names
    This prevents the naming conflicts when ATM shifts
    """
    global strike_pair_candles

    if strike_pair_data is None or strike_pair_data.empty:
        return

    with candle_lock:
        current_time = datetime.now(IST)
        current_date = current_time.date()
        
        for _, row in strike_pair_data.iterrows():
            indicator_key = row['IndicatorKey']  # e.g., "82000CE_81000PE"
            open_price = row['Combined_Open']
            high_price = row['Combined_High']
            low_price = row['Combined_Low']
            close_price = row['Combined_Close']
            cumulative_volume = row['Combined_Volume']

            # Ensure structure exists for this indicator key
            if indicator_key not in strike_pair_candles:
                strike_pair_candles[indicator_key] = {}
            if current_date not in strike_pair_candles[indicator_key]:
                strike_pair_candles[indicator_key][current_date] = {}
            if '1min' not in strike_pair_candles[indicator_key][current_date]:
                strike_pair_candles[indicator_key][current_date]['1min'] = {}

            # Calculate incremental volume (prevent negative values)
            cache_key = (indicator_key, row['Expiry'])
            last_volume = fetcher.last_volume_cache.get(cache_key, 0)
            incremental_volume = max(0, cumulative_volume - last_volume)  # Prevent negative volume
            fetcher.last_volume_cache[cache_key] = cumulative_volume

            # Create/update 1-minute candle
            minute_timestamp = row['Timestamp'].replace(second=0, microsecond=0)
            candle_end_time = minute_timestamp + timedelta(minutes=1)
            
            candles_dict = strike_pair_candles[indicator_key][current_date]['1min']
            
            if minute_timestamp not in candles_dict:
                candles_dict[minute_timestamp] = {
                    'open': open_price,
                    'high': high_price,
                    'low': low_price,
                    'close': close_price,
                    'volume': incremental_volume,
                    'complete': current_time >= candle_end_time,
                    'end_time': candle_end_time
                }
                
                # Calculate and store RSI for this candle set
                rsi_values = calculate_rsi(candles_dict)
                if rsi_values:
                    # Store RSI values in candles (keyed by timestamp)
                    sorted_timestamps = sorted(candles_dict.keys())
                    for i, ts in enumerate(sorted_timestamps):
                        if i < len(rsi_values):
                            candles_dict[ts]['rsi'] = rsi_values[i]
            else:
                candle = candles_dict[minute_timestamp]
                # For existing candles, we update high/low if the new values are more extreme
                # and always update close with the latest value
                candle['high'] = max(candle['high'], high_price)
                candle['low'] = min(candle['low'], low_price)
                candle['close'] = close_price
                candle['volume'] += incremental_volume
                candle['complete'] = current_time >= candle_end_time
                
                # Recalculate and update RSI for all candles when data changes
                rsi_values = calculate_rsi(candles_dict)
                if rsi_values:
                    sorted_timestamps = sorted(candles_dict.keys())
                    for i, ts in enumerate(sorted_timestamps):
                        if i < len(rsi_values):
                            candles_dict[ts]['rsi'] = rsi_values[i]

            # Mark completed candles
            for ts in list(candles_dict.keys()):
                candle = candles_dict[ts]
                if current_time >= candle['end_time'] and not candle['complete']:
                    candle['complete'] = True

def calculate_rsi(candles, period=14, fill_empty=True):
    """Calculate RSI from candles, handling incomplete initial data
    
    Args:
        candles (dict): Dictionary of candle data {timestamp: candle}
        period (int): RSI lookback period (default 14)
        fill_empty (bool): Whether to fill with None when not enough data
    
    Returns:
        list: RSI values matching the length of input candles
    """
    # Get all candles in chronological order (complete and incomplete)
    sorted_ts = sorted(candles.keys())
    all_candles = [candles[ts] for ts in sorted_ts]
    
    # Get only complete candles for RSI calculation
    complete_candles = [candle for candle in all_candles 
                       if candle.get('complete', True)]
    
    # Handle case where we don't have enough complete data
    if len(complete_candles) <= 1:
        return [100] * len(all_candles)
    
    if len(complete_candles) <= period:
        if fill_empty:
            return [100] * len(all_candles)
        return []
    
    # Calculate price changes from complete candles only
    deltas = []
    for i in range(1, len(complete_candles)):
        change = complete_candles[i]['close'] - complete_candles[i-1]['close']
        deltas.append(change)
    
    # Separate gains and losses
    gains = [max(delta, 0) for delta in deltas]
    losses = [max(-delta, 0) for delta in deltas]
    
    # Initialize averages
    avg_gain = sum(gains[:period]) / period
    avg_loss = sum(losses[:period]) / period
    
    rsi_values = []
    
    # First RSI value
    first_rsi = 100.0 if avg_loss == 0 else 100 - (100 / (1 + (avg_gain / avg_loss)))
    rsi_values.append(first_rsi)
    
    # Calculate subsequent values with smoothing
    for i in range(period, len(deltas)):
        avg_gain = (avg_gain * (period - 1) + gains[i]) / period
        avg_loss = (avg_loss * (period - 1) + losses[i]) / period
        
        if avg_loss == 0:
            rsi = 100.0
        else:
            rs = avg_gain / avg_loss
            rsi = 100 - (100 / (1 + rs))
        rsi_values.append(rsi)
    
    # Create result array matching all_candles length
    result = [100] * len(all_candles)  # Default to 100
    
    # Map complete candle RSI values back to all candles
    complete_index = 0
    for i, candle in enumerate(all_candles):
        if candle.get('complete', True):
            if complete_index < len(complete_candles) - len(rsi_values):
                # This is before we have enough data for RSI
                result[i] = 100
            elif complete_index < len(complete_candles):
                # Map RSI value from the calculated values
                rsi_index = complete_index - (len(complete_candles) - len(rsi_values))
                if 0 <= rsi_index < len(rsi_values):
                    result[i] = rsi_values[rsi_index]
            complete_index += 1
        else:
            # For incomplete candles, use the last complete candle's RSI
            if complete_index > 0:
                # Find the last complete candle's RSI
                last_complete_rsi_index = complete_index - 1 - (len(complete_candles) - len(rsi_values))
                if 0 <= last_complete_rsi_index < len(rsi_values):
                    result[i] = rsi_values[last_complete_rsi_index]
    
    return result

def calculate_anchored_vwap(candles):
    """Calculate anchored VWAP from complete candles"""
    cumulative_pv = 0
    cumulative_vol = 0
    vwap_values = []
    for ts in sorted(candles.keys()):
        candle = candles[ts]
        if not candle['complete']:
            continue
        typical_price = candle['close']
        volume = max(0, candle['volume'])
        cumulative_pv += typical_price * volume
        cumulative_vol += volume
        if cumulative_vol > 0:
            vwap_values.append(cumulative_pv / cumulative_vol)
    return vwap_values

def calculate_ema(values, period=24):
    """Calculate EMA from values"""
    if not values or len(values) < 3:
        return []
    ema_values = []
    smoothing = 2 / (period + 1)
    ema = sum(values[:3]) / 3
    ema_values.extend([ema] * 3)
    for value in values[3:]:
        ema = (value - ema) * smoothing + ema
        ema_values.append(ema)
    return ema_values

