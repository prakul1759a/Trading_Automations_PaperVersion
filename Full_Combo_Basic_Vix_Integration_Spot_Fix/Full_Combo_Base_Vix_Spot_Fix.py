    def monitor_open_trades(self, options_df, spot_price, zerodha_spot_cache=None):
        """Monitor all open trades and update current info

        Args:
            zerodha_spot_cache: Pre-fetched Zerodha spot (fetched once per cycle in main loop)
        """
        # Check if enough time has passed to log price_update events (reduces disk I/O)
        current_time = time_mod.time()
        should_log_price_update = (current_time - self.last_price_update_log_time) >= PRICE_UPDATE_LOG_INTERVAL

        for trade_id in list(self.open_trades.keys()):
            try:
                trade = self.open_trades[trade_id]
                level_name = trade['level_name']

                # Get current prices
                level_df = options_df[options_df['Level'] == level_name]
                if level_df.empty:
                    continue

                ce_data = level_df[level_df['Type'] == 'CE'].iloc[0]
                pe_data = level_df[level_df['Type'] == 'PE'].iloc[0]

                # Get display LTP (Zerodha cache already refreshed in main loop)
                display_ce_ltp, display_pe_ltp = get_display_ltp(
                    trade['ce_strike'],
                    trade['pe_strike'],
                    ce_data['Expiry'],
                    ce_data['Close'],
                    pe_data['Close']
                )

                # Use pre-fetched Zerodha spot from main loop (no redundant disk read)
                display_spot = zerodha_spot_cache if zerodha_spot_cache is not None else spot_price

                # ALWAYS update current info in memory (every cycle) - this is critical for trade state
                trade['current_info'] = {
                    'price': ce_data['Close'] + pe_data['Close'],  # Algotest for calculations
                    'spot': spot_price,  # Algotest spot for state
                    'timestamp': datetime.now(IST),
                    'call_ltp': ce_data['Close'],  # Algotest for state
                    'put_ltp': pe_data['Close']    # Algotest for state
                }

                # Only LOG price_update events every PRICE_UPDATE_LOG_INTERVAL seconds (reduces disk I/O)
                # Trade state is still updated above every cycle - only the log write is throttled
                if should_log_price_update:
                    self.log_trade_event('price_update', {
                        'trade_id': trade_id,
                        'call_ltp': display_ce_ltp,  # Zerodha LTP for display
                        'put_ltp': display_pe_ltp,   # Zerodha LTP for display
                        'spot': display_spot,  # Zerodha spot for display
                        'strangle_price': display_ce_ltp + display_pe_ltp,  # Zerodha total for display
                        'strike': [trade['ce_strike'], trade['pe_strike']],  # Array format for JSON
                        'strikes': trade['ce_strike'],  # For backward compatibility with GUI
                        # Enhanced system extras (for GUI compatibility)
                        'strangle_name': level_name,  # GUI looks for this field
                        'level_name': level_name,
                        'indicator_key': trade['indicator_key'],
                        'timestamp': datetime.now(IST)
                    })

            except Exception as e:
                logging.error(f"Monitoring failed for {trade_id}: {str(e)}", exc_info=True)

        # Update timestamp after logging (outside the loop so it's updated once per call)
        if should_log_price_update:
            self.last_price_update_log_time = current_time

    def force_exit(self, xt, options_df, spot_price, reason):
        """Force exit all open trades"""
        exited_trades = []
        
        # If no options data available (emergency exit), just mark trades as closed
        if options_df is None or options_df.empty:
            logging.info(f"Emergency force exit without options data - {reason}")
            for trade_id in list(self.open_trades.keys()):
                trade = self.open_trades[trade_id]
                
                # Mark trade as force exited without current prices
                trade.update({
                    'exit_time': datetime.now(IST),
                    'exit_spot': spot_price,
                    'call_ltp_exit': 0,  # Unknown at emergency exit
                    'put_ltp_exit': 0,   # Unknown at emergency exit
                    'exit_signal': {'signal': 0, 'timestamp': datetime.now(IST)}
                })
                
                self.closed_trades.append(trade)
                exited_trades.append(trade_id)
                
                self.log_trade_event('force_exit', {
                    'trade_id': trade_id,
                    'exit_spot': spot_price,
                    'call_ltp': 0,
                    'put_ltp': 0,
                    'signal': {'signal': 0},
                    'reason': reason,
                    'level_name': trade['level_name'],
                    'indicator_key': trade['indicator_key'],
                    'emergency_exit': True
                })
            
            # Clear all open trades
            self.open_trades.clear()
            
        else:
            # Normal force exit with options data
            for trade_id in list(self.open_trades.keys()):
                exit_signal = {trade['level_name']: {'signal': 0, 'timestamp': datetime.now(IST)} 
                              for trade in [self.open_trades[trade_id]]}
                if self.check_exit(xt, trade_id, exit_signal, options_df, spot_price):
                    exited_trades.append(trade_id)
        
        if exited_trades:
            logging.info(f"Force exited {len(exited_trades)} trades due to: {reason}")
        else:
            logging.info("No open trades to force exit")

