    # Need both locks: eligibility_lock for ACTIVE_STRIKE_PAIRS, candle_lock for strike_pair_candles
    with eligibility_lock:
        active_pairs_copy = {k: v.copy() for k, v in ACTIVE_STRIKE_PAIRS.items()}
    
    with candle_lock:
        for level_name, pair_data in active_pairs_copy.items():
            if not pair_data['is_active']:
                continue
                
            indicator_key = pair_data['indicator_key']
            
            if indicator_key not in strike_pair_candles:
                continue
            if date_val not in strike_pair_candles[indicator_key]:
                continue
            if '1min' not in strike_pair_candles[indicator_key][date_val]:
                continue
                
            candles = strike_pair_candles[indicator_key][date_val]['1min']
            complete_candles = [c for c in candles.values() if c['complete']]
            
            # Need minimum candles
            min_candles = 15 if USE_RSI_STRATEGY else 10
            if len(complete_candles) < min_candles:
                continue

            complete_candles.sort(key=lambda x: x['end_time'])
            last_candle = complete_candles[-1]

            # Calculate RSI signal if enabled
            rsi_entry = rsi_exit = None
            if USE_RSI_STRATEGY:
                if 'rsi' not in last_candle or last_candle['rsi'] is None:
                    continue
                last_rsi = last_candle['rsi']
                rsi_entry = (last_rsi >= 70)
                rsi_exit = (last_rsi < 30)
            else:
                last_rsi = None

            # Calculate VWAP signal if enabled
            vwap_entry = vwap_exit = None
            last_vwap = last_ema = None
            if USE_VWAP_STRATEGY:
                vwap_values = calculate_anchored_vwap(candles)
                ema_values = calculate_ema(vwap_values)
                if vwap_values and ema_values:
                    last_vwap = vwap_values[-1]
                    last_ema = ema_values[-1]
                    vwap_entry = (last_vwap < last_ema)
                    vwap_exit = (last_vwap >= last_ema)
                else:
                    continue

            # Combined signal logic
            if USE_RSI_STRATEGY and USE_VWAP_STRATEGY:
                signal_entry = rsi_entry and vwap_entry  # BOTH must be true
                signal_exit = rsi_exit or vwap_exit      # EITHER triggers exit
            elif USE_RSI_STRATEGY:
                signal_entry = rsi_entry
                signal_exit = rsi_exit
            elif USE_VWAP_STRATEGY:
                signal_entry = vwap_entry
                signal_exit = vwap_exit
            else:
                continue  # No strategy enabled

            if signal_exit:
                signal_value = 0
            elif signal_entry:
                signal_value = 1
            else:
                prev_signal = pair_data.get('last_signal')
                signal_value = prev_signal.get('signal', 0) if prev_signal else 0

            signals[level_name] = {
                'indicator_key': indicator_key,
                'timestamp': last_candle['end_time'] - timedelta(minutes=1),
                'rsi': last_rsi,
                'vwap': last_vwap,
                'ema': last_ema,
                'signal': signal_value,
                'ce_strike': pair_data['ce_strike'],
                'pe_strike': pair_data['pe_strike'],
                'cumulative_volume': last_candle['volume']
            }
            
    # Update last signal for all pairs in the original ACTIVE_STRIKE_PAIRS (outside candle_lock)
    if signals:
        with eligibility_lock:
            for level_name, signal_data in signals.items():
                if level_name in ACTIVE_STRIKE_PAIRS:
                    ACTIVE_STRIKE_PAIRS[level_name]['last_signal'] = signal_data
    
    return signals if signals else None

