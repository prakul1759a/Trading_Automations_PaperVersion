def get_sensex_spot_from_csv():
    """Read latest SENSEX spot price from local Zerodha spot CSV.
    File: SENSEX_SPOT_CSV_BASE/backend/DDMMYYYY/SPOT/SENSEX/YYYYMMDD_SENSEX_SPOT.csv
    Uses binary seek to read only the last line - ~0.1ms, no network dependency.
    Returns float spot price, or None if file missing/unreadable.
    """
    try:
        now    = datetime.now(IST)
        folder = now.strftime('%d%m%Y')
        fname  = now.strftime('%Y%m%d') + '_SENSEX_SPOT.csv'
        path   = os.path.join(SENSEX_SPOT_CSV_BASE, 'backend', folder, 'SPOT', 'SENSEX', fname)
        if not os.path.exists(path):
            return None
        with open(path, 'rb') as f:
            # Seek to near end to find last line without loading whole file
            try:
                f.seek(-2, os.SEEK_END)
            except OSError:
                return None  # file too small (header only)
            while f.read(1) != b'\n':
                try:
                    f.seek(-2, os.SEEK_CUR)
                except OSError:
                    f.seek(0)
                    break
            last = f.readline().decode('utf-8', errors='ignore').strip()
        if not last or ',' not in last:
            return None
        parts = last.split(',')
        if parts[0].startswith('recv'):   # skip header row if file is tiny
            return None
        spot = float(parts[1])
        return spot if spot > 0 else None
    except Exception:
        return None


def get_sensex_spot_bse1(session, keep_trying=False):
    """
    Fetches the latest SENSEX spot (cash index) using Algotest's /historical endpoint for BSE_1.
    Returns: (float) spot price, (str) timestamp
    
    Args:
        session: requests session object
        keep_trying: if True, will retry indefinitely until successful
    """
    url = "https://prices.algotest.in/historical"
    attempt = 0
    
    while True:
        attempt += 1
        now = datetime.now(IST)  # Use IST consistently
        today = now.date()
        
        try:
            # Try different time windows based on attempt
            if attempt == 1:
                # Current approach - from market open
                start = datetime.combine(today, time(9, 15))
            elif attempt == 2:
                # Try from 5 minutes ago
                start = now - timedelta(minutes=5)
            elif attempt == 3:
                # Try from 15 minutes ago
                start = now - timedelta(minutes=15)
            elif attempt == 4:
                # Try from 30 minutes ago
                start = now - timedelta(minutes=30)
            else:
                # For subsequent attempts, try from 1 hour ago and keep current time window
                start = now - timedelta(minutes=60)
            
            params = {
                "tokens": "BSE_1",
                "start_dt": start.strftime("%Y-%m-%dT%H:%M:%S"),
                "end_dt": now.strftime("%Y-%m-%dT%H:%M:%S")
            }
            
            logging.info(f"Spot fetch attempt {attempt}: {start.strftime('%H:%M:%S')} to {now.strftime('%H:%M:%S')}")
            resp = session.get(url, params=params, timeout=10)
            resp.raise_for_status()
            data = resp.json().get("BSE_1", {})
            closes = data.get("close", [])
            timestamps = data.get("timestamp", [])
            
            if closes and timestamps:
                spot_price = float(closes[-1])
                logging.info(f"SUCCESS: Fetched spot price: {spot_price}")
                return spot_price, timestamps[-1]
            else:
                logging.warning(f"Attempt {attempt}: No data returned from API")
                
        except Exception as e:
            logging.error(f"Spot fetch attempt {attempt} failed: {str(e)}")
        
        # If not keep_trying, return None after a few attempts
        if not keep_trying and attempt >= 5:
            logging.error("Failed to fetch spot price after 5 attempts")
            return None, None
            
        # Wait before retry - progressive backoff but not too long
        if attempt <= 3:
            wait_time = 5  # 5 seconds for first few attempts
        elif attempt <= 10:
            wait_time = 15  # 15 seconds for next attempts
        else:
            wait_time = 30  # 30 seconds for later attempts
            
        logging.info(f"Waiting {wait_time}s before next attempt... (Market data may not be available yet)")
        time_mod.sleep(wait_time)

def initialize_fixed_atm_levels():
    """
    Calculate all ATM levels once at program start and never change them.
    This prevents the strangle naming conflicts caused by dynamic ATM shifts.
    """
    global INITIAL_SPOT, FIXED_ATM_LEVELS, ACTIVE_STRIKE_PAIRS
    
    # Get initial spot price - try local CSV first, fall back to Algotest HTTP
    logging.info("Waiting for SENSEX spot price from market data...")
    logging.info("Program will start once market data becomes available...")

    spot = get_sensex_spot_from_csv()
    if spot is not None:
        logging.info(f"Got spot from CSV: {spot}")
    else:
        logging.info("CSV spot unavailable, falling back to Algotest HTTP...")
        spot, timestamp = get_sensex_spot_bse1(session, keep_trying=True)
        if spot is None:
            logging.error("Critical: Unexpected failure in spot price fetching")
            raise RuntimeError("Unexpected error in spot price fetching")
        logging.info(f"SUCCESS: Got spot price: {spot} (timestamp: {timestamp})")

    logging.info("STARTING: Trading system initialized with live market data!")
    
    INITIAL_SPOT = spot
    initial_atm = round(spot / ATM_INTERVAL) * ATM_INTERVAL
    
    logging.info(f"Initial Spot: {INITIAL_SPOT}, Initial ATM: {initial_atm}")
    
    # Calculate fixed ATM levels
    FIXED_ATM_LEVELS.clear()
    ACTIVE_STRIKE_PAIRS.clear()
    
    # Create ATM levels: 3 below, center, 3 above
    for i in range(-ATM_LEVELS_COUNT, ATM_LEVELS_COUNT + 1):
        level_atm = initial_atm + (i * ATM_INTERVAL)
        level_name = f"ATM_LV{i:+d}"  # e.g., ATM_LV-3, ATM_LV+0, ATM_LV+3
        
        # Calculate CE and PE strikes (straddle or strangle based on config)
        if USE_STRADDLE:
            ce_strike = level_atm  # Straddle: same strike
            pe_strike = level_atm
        else:
            ce_strike = level_atm + STRANGLE_OFFSET  # Strangle: different strikes
            pe_strike = level_atm - STRANGLE_OFFSET
        
        # Generate indicator key based on straddle/strangle mode
        if USE_STRADDLE:
            indicator_key = f"{ce_strike}_STRADDLE"
        else:
            indicator_key = f"{ce_strike}CE_{pe_strike}PE"

        FIXED_ATM_LEVELS[level_name] = {
            'atm': level_atm,
            'ce_strike': ce_strike,
            'pe_strike': pe_strike,
            'strike_pair_key': indicator_key
        }

        # Initialize active strike pairs with trade eligibility based on proximity to initial spot
        # Only the closest 3 levels can trade initially, others wait until spot moves near them
        is_trade_eligible = abs(i) <= 1  # Only -1, 0, +1 can trade initially (3 levels)

        ACTIVE_STRIKE_PAIRS[level_name] = {
            'ce_strike': ce_strike,
            'pe_strike': pe_strike,
            'is_active': True,  # Always active for calculation
            'can_enter_trade': is_trade_eligible,  # Can only enter trades when near current spot
            'last_signal': None,
            'indicator_key': indicator_key,
            'atm_level': level_atm,
            'level_index': i
        }
    
    logging.info("Fixed ATM Levels initialized:")
    for level_name, level_data in FIXED_ATM_LEVELS.items():
        trade_status = "TRADE_ELIGIBLE" if ACTIVE_STRIKE_PAIRS[level_name]['can_enter_trade'] else "CALC_ONLY"
        logging.info(f"{level_name}: ATM={level_data['atm']}, CE={level_data['ce_strike']}, PE={level_data['pe_strike']} [{trade_status}]")
    
    return FIXED_ATM_LEVELS

def eligibility_background_processor():
    """
    Background thread that processes eligibility updates without blocking main thread.
    This prevents candle processing delays during complex eligibility calculations.
    """
    global eligibility_thread_alive, eligibility_queue
    eligibility_thread_alive.set()
    
    logging.info("Eligibility background processor started")
    
    try:
        while True:
            try:
                # Get spot price from queue (blocks until available)
                current_spot = eligibility_queue.get(timeout=10)
                
                # Perform eligibility update in background
                update_trade_eligibility_by_spot_internal(current_spot)
                
            except queue.Empty:
                continue  # Continue loop on timeout
            except Exception as e:
                logging.error(f"Error in eligibility background processor: {e}")
                continue
                
    except Exception as e:
        logging.error(f"Eligibility background processor crashed: {e}")
    finally:
        eligibility_thread_alive.clear()
        logging.error("Eligibility background processor stopped")

def update_trade_eligibility_by_spot(current_spot):
    """
    Queue spot price for background eligibility processing.
    This prevents main thread blocking during candle processing.
    """
    global eligibility_queue
    
    if eligibility_queue is not None:
        try:
            # Non-blocking put - if queue is full, skip this update
            eligibility_queue.put_nowait(current_spot)
        except queue.Full:
            # Skip if queue is full - eligibility updates are not critical for candle processing
            pass

def update_trade_eligibility_by_spot_internal(current_spot):
    """
    Internal function that performs actual eligibility calculation.
    Now runs in background thread to prevent main thread blocking.
    
    PERFORMANCE FIX: Added throttling + background threading to prevent blocking candle processing.
    """
    global ACTIVE_STRIKE_PAIRS, last_eligibility_update_time, eligibility_lock
    
    # Performance optimization: Only update eligibility every 30 seconds
    # This prevents excessive CPU usage even in background thread
    now = datetime.now(IST)
    if last_eligibility_update_time is not None:
        time_since_last_update = (now - last_eligibility_update_time).total_seconds()
        if time_since_last_update < 5:  # Skip if updated within last 5 seconds
            return
    
    logging.info(f"[BG] Updating trade eligibility based on spot {current_spot:.2f}")
    last_eligibility_update_time = now
    
    # Thread-safe access to ACTIVE_STRIKE_PAIRS
    with eligibility_lock:
        if not ACTIVE_STRIKE_PAIRS:
            return
        
        # Find the closest ATM level to current spot
        current_atm = round(current_spot / ATM_INTERVAL) * ATM_INTERVAL
        
        # Calculate which levels should be eligible - exactly the 3 closest to current ATM
        eligible_changes = []
        
        # Sort levels by distance from current ATM and select closest 3
        level_distances = []
        for level_name, pair_data in ACTIVE_STRIKE_PAIRS.items():
            level_atm = pair_data['atm_level']
            distance_from_current = abs(level_atm - current_atm)
            level_distances.append((level_name, pair_data, distance_from_current))
        
        # Sort by distance and take closest 3
        level_distances.sort(key=lambda x: x[2])
        closest_3_levels = set(item[0] for item in level_distances[:3])
        
        for level_name, pair_data in ACTIVE_STRIKE_PAIRS.items():
            # Level is eligible if it's one of the 3 closest to current spot ATM
            should_be_eligible = level_name in closest_3_levels
            
            was_eligible = pair_data['can_enter_trade']
            
            if should_be_eligible != was_eligible:
                pair_data['can_enter_trade'] = should_be_eligible
                level_atm = pair_data['atm_level']
                distance_from_current = abs(level_atm - current_atm)
                eligible_changes.append({
                    'level': level_name,
                    'atm': level_atm, 
                    'new_status': should_be_eligible,
                    'distance': distance_from_current
                })
    
    # Log outside the lock to minimize lock time
    if eligible_changes:
        logging.info(f"[BG] Trade eligibility updated based on spot {current_spot:.2f} (ATM: {current_atm})")
        for change in eligible_changes:
            status = "TRADE_ELIGIBLE" if change['new_status'] else "CALC_ONLY"
            logging.info(f"[BG]   {change['level']} (ATM: {change['atm']}) -> {status}")
    
    # Log current eligible levels for debugging (only occasionally)
    if random.randint(1, 60) == 1:  # Every ~1 minute on average
        with eligibility_lock:
            eligible_levels = [level for level, data in ACTIVE_STRIKE_PAIRS.items() if data['can_enter_trade']]
        logging.info(f"[BG] Currently TRADE_ELIGIBLE levels: {eligible_levels}")

# Persistent SQLite connection for Algotest DB (thread-safe with check_same_thread=False)
_algotest_db_conn = None
_algotest_db_lock = Lock()

def _get_algotest_db_conn():
    """Get or create persistent SQLite connection to today's Algotest DB (read-only mode)"""
    global _algotest_db_conn
    if _algotest_db_conn is None:
        # Open in read-only mode to prevent blocking the data collector's writes
        # URI mode with ?mode=ro ensures we can't accidentally write
        # timeout=30 handles transient locks gracefully
        db_path = get_algotest_db_path('SENSEX')
        db_uri = f"file:{db_path}?mode=ro"
        _algotest_db_conn = sqlite3.connect(db_uri, uri=True, timeout=30.0, check_same_thread=False)
    return _algotest_db_conn

def load_latest_from_db(symbol, strike, opt_type):
    """
    Load latest candle data from Algotest SQLite DB (latest_prices table).
    Replaces load_latest_from_csv.

    Args:
        symbol: 'SENSEX' (used for logging only, DB is per-symbol)
        strike: Strike price (int)
        opt_type: 'CE' or 'PE'

    Returns:
        dict with keys: token, open, high, low, close, volume, timestamp
        None if no data found
    """
    try:
        with _algotest_db_lock:
            conn = _get_algotest_db_conn()
            cursor = conn.cursor()
            cursor.execute("""
                SELECT token, open, high, low, close, vol_in_day, bar_timestamp
                FROM latest_prices
                WHERE strike = ? AND opt_type = ?
            """, (int(strike), opt_type))
            row = cursor.fetchone()

        if not row:
            logging.warning(f"No DB data for {symbol} strike={strike} opt_type={opt_type}")
            return None

        token, open_p, high_p, low_p, close_p, volume, bar_timestamp = row

        # Parse bar_timestamp (naive string like "2026-02-06T10:04:00") -> IST-aware
        # bar_timestamp is end-of-candle, subtract 1 minute for candle start
        dt = datetime.strptime(bar_timestamp, "%Y-%m-%dT%H:%M:%S")
        ts_ist = IST.localize(dt) - timedelta(minutes=1)

        return {
            "token": token,
            "open": float(open_p),
            "high": float(high_p),
            "low": float(low_p),
            "close": float(close_p),
            "volume": int(volume) if volume is not None else 0,
            "timestamp": ts_ist
        }

    except Exception as e:
        logging.error(f"Error loading from DB for {symbol} strike={strike} opt_type={opt_type}: {e}")
        return None

def is_in_data_buffer():
    """
    Check if we're in the data buffer period (first X seconds of new minute).
    This prevents reading CSV files while new data is being written.
    """
    current_now = datetime.now(IST)
    seconds_past_minute = current_now.second

    # Buffer active during first DATA_BUFFER_SECONDS of each minute
    if seconds_past_minute < DATA_BUFFER_SECONDS:
        if seconds_past_minute == 0:  # Log only at start of minute to avoid spam
            logging.info(f"[DATA_BUFFER] Pausing data reading for {DATA_BUFFER_SECONDS}s (new data being written) - {current_now.strftime('%H:%M:%S')}")
        return True
    elif seconds_past_minute == DATA_BUFFER_SECONDS:
        logging.info(f"[DATA_BUFFER] Resuming data reading - {current_now.strftime('%H:%M:%S')}")
    return False

def is_in_trading_buffer():
    """
    Check if we're in the synchronized trading buffer period.
    Covers: 59s of current minute + entire data buffer period.
    This ensures no trading decisions during data transition period.
    """
    # Quick disable: If TRADING_BUFFER_SECONDS is 0 or less, buffer is fully disabled
    if TRADING_BUFFER_SECONDS <= 0:
        return False

    current_now = datetime.now(IST)
    seconds_past_minute = current_now.second

    # Trading buffer spans two periods:
    # 1. Last second of current minute (59s)
    # 2. First DATA_BUFFER_SECONDS of next minute
    buffer_start = 60 - 1  # Start at 59 seconds

    if (seconds_past_minute >= buffer_start) or (seconds_past_minute < DATA_BUFFER_SECONDS):
        if seconds_past_minute == buffer_start:  # Log only at start to avoid spam
            logging.info(f"[TRADING_BUFFER] Pausing trading decisions for {TRADING_BUFFER_SECONDS}s (data transition period) - {current_now.strftime('%H:%M:%S')}")
        return True
    elif seconds_past_minute == DATA_BUFFER_SECONDS:
        logging.info(f"[TRADING_BUFFER] Resuming trading decisions - {current_now.strftime('%H:%M:%S')}")
    return False

