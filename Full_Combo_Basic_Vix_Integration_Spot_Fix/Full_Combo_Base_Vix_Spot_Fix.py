def fetch_and_process_data(fetcher, data_queue):
    """Main data processing loop with health monitoring"""
    global data_thread_alive
    data_thread_alive.set()  # Signal thread is alive
    
    try:
        while True:
            # Check market hours in data thread too
            timings = get_market_timings()
            now = datetime.now(IST)  # Use IST
            if now >= timings['kill_time']:
                logging.info("Data thread: Kill time reached - stopping data collection")
                break
            
            start_time = time_mod.time()
            
            # Configurable buffer to avoid CE/PE file race conditions
            if is_in_data_buffer():
                elapsed = time_mod.time() - start_time
                sleep_time = max(0, 1 - elapsed)
                time_mod.sleep(sleep_time)
                data_thread_alive.set()  # Maintain heartbeat
                continue
            
            options_df = fetcher.fetch_options_chain_for_active_pairs()

            if options_df is not None and not options_df.empty:
                try:
                    strike_pair_data = analyze_strike_pairs(options_df.copy())
                    if strike_pair_data is not None and not strike_pair_data.empty:
                        data_queue.put((options_df, strike_pair_data))  # Normal blocking put is fine
                        
                        update_candles_by_indicator_key(strike_pair_data.copy(), fetcher)
                        save_candles(fetcher.last_volume_cache)
                        
                        for _, row in strike_pair_data.iterrows():
                            logging.info(f"Updated {row['Level']}: {row['IndicatorKey']} "
                                       f"Close: {row['Combined_Close']:.2f} "
                                       f"Volume: {row['Combined_Volume']}")
                    else:
                        logging.warning("Strike pair analysis failed or no data")
                except Exception as e:
                    logging.error(f"Error in strike pair analysis: {str(e)}")

            elapsed = time_mod.time() - start_time
            sleep_time = max(0, 1 - elapsed)
            time_mod.sleep(sleep_time)
            data_thread_alive.set()  # Periodic heartbeat
    except Exception as e:
        logging.error(f"Data thread error: {e}")
    finally:
        data_thread_alive.clear()  # Signal thread is dead
        logging.info("Data thread terminated")

