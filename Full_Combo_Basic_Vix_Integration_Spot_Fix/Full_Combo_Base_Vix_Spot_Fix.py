def load_candles():
    """Load candles with retry logic for file locking issues"""
    global strike_pair_candles

    max_retries = 5
    retry_delay = 0.1  # 100ms initial delay (longer than save since this is startup)

    with candle_lock:
        for attempt in range(max_retries):
            try:
                with open(get_date_dir(), 'rb') as f:
                    loaded_data = pickle.load(f)

                # Handle both old format (dict) and new format (dict with volume_cache)
                if isinstance(loaded_data, dict):
                    if '_volume_cache_' in loaded_data:
                        # New format: includes volume cache
                        strike_pair_candles = loaded_data['candles']
                        volume_cache = loaded_data['_volume_cache_']
                        logging.info(f"Loaded strike-pair candle data with {len(volume_cache)} volume cache entries.")
                        return volume_cache  # Return cache to be restored in fetcher
                    else:
                        # Old format: just candles (backward compatible)
                        strike_pair_candles = loaded_data
                        logging.info("Loaded strike-pair candle data from storage (no volume cache).")
                        return {}  # Empty cache for old format

                return {}  # Success!

            except FileNotFoundError:
                logging.info("No previous candle data found for today.")
                strike_pair_candles = {}
                return {}  # Empty cache

            except (PermissionError, OSError, EOFError, pickle.UnpicklingError) as e:
                # Retry on file locking or corruption errors
                if attempt < max_retries - 1:
                    logging.warning(f"Candle load attempt {attempt+1}/{max_retries} failed: {e}. Retrying...")
                    time_mod.sleep(retry_delay * (2 ** attempt))  # Exponential backoff
                    continue
                else:
                    # Final attempt failed - reset to empty
                    logging.error(f"Failed to load candles after {max_retries} attempts: {e}. Starting fresh.")
                    strike_pair_candles = {}
                    return {}  # Empty cache

            except Exception as e:
                # Unknown error - don't retry, just reset
                logging.error(f"Unexpected error loading candle data: {e}. Starting fresh.")
                strike_pair_candles = {}
                return {}  # Empty cache

