def write_orders_batch(orders_list, max_retries=10, retry_delay=0.2):
    """
    Write multiple orders to orders.csv in a single operation (batch write)

    This is more efficient than individual writes as it:
    - Opens file once instead of N times
    - Acquires lock once instead of N times
    - Reduces PermissionError risk

    Args:
        orders_list (list): List of order tuples, each: (order_id, token, value, price, quantity, strategy_id)
        max_retries (int): Maximum retry attempts (default 10)
        retry_delay (float): Initial retry delay in seconds (default 0.2)

    Returns:
        bool: True if successful, False if failed after all retries
    """
    csv_path = r"D:\XQG\ABC_Full_Combo_ordeer.csv"

    for attempt in range(max_retries):
        try:
            with orders_csv_lock:
                with open(csv_path, 'a', newline='', encoding='utf-8') as f:
                    writer = csv.writer(f)

                    # Write all orders at once
                    for order_id, token, value, price, quantity, strategy_id in orders_list:
                        writer.writerow([order_id, token, 'L', value, price, quantity, strategy_id])

                    f.flush()
                    # os.fsync(f.fileno())  # Force write to physical disk

            logging.info(f" Batch: {len(orders_list)} orders written to CSV (attempt {attempt + 1})")
            for order_id, token, value, price, _, _ in orders_list:
                logging.info(f"   - {order_id} | Token:{token} | Value:{value} | Price:{price}")
            return True

        except (PermissionError, BlockingIOError) as e:
            if attempt < max_retries - 1:
                sleep_time = retry_delay * (2 ** attempt) + (0.1 * attempt)
                logging.warning(f" Batch write attempt {attempt + 1}/{max_retries} failed: {e}. Retrying in {sleep_time:.2f}s...")
                time_mod.sleep(sleep_time)
            else:
                logging.error(f" CRITICAL: Batch write failed after {max_retries} attempts: {e}")
                logging.error(f" LOST {len(orders_list)} ORDERS")
                return False

        except Exception as e:
            logging.error(f" Unexpected error in batch write: {e}")
            return False

    return False

class ZerodhaMongoReader:
    """
    MongoDB-based Zerodha LTP reader - replaces CSV-based ZerodhaLTPReader.
    Reads from MarketWatch_Live.ticks_YYYYMMDD collection.
    """

    def __init__(self, mongo_uri, db_name):
        self.client = MongoClient(mongo_uri, serverSelectionTimeoutMS=5000)
        self.db = self.client[db_name]
        self.collection_name = f"ticks_{datetime.now(IST).strftime('%Y%m%d')}"
        self.collection = self.db[self.collection_name]
        self.strike_cache = {}
        self.last_refresh_time = 0
        self.refresh_interval = 0.5
        self.lock = Lock()
        self.cache_size = 0
        logging.info(f"ZerodhaMongoReader initialized: {db_name}.{self.collection_name}")

    def get_snapshot_path(self):
        return f"MongoDB: {self.db.name}.{self.collection_name}"

    def refresh_cache(self, force=False):
        current_time = time_mod.time()
        if not force and (current_time - self.last_refresh_time) < self.refresh_interval:
            return True

        try:
            recent_cutoff = datetime.now(IST) - timedelta(seconds=30)
            pipeline = [
                {"$match": {"timestamp": {"$gte": recent_cutoff}, "category": "OPTIONS"}},
                {"$sort": {"timestamp": -1}},
                {"$group": {
                    "_id": {"name": "$name", "strike": "$strike", "instrument_type": "$instrument_type"},
                    "last_price": {"$first": "$last_price"},
                    "timestamp": {"$first": "$timestamp"},
                    "instrument_token": {"$first": "$instrument_token"}
                }}
            ]

            results = list(self.collection.aggregate(pipeline))

            with self.lock:
                self.strike_cache.clear()
                for doc in results:
                    name = doc["_id"]["name"]
                    strike = doc["_id"]["strike"]
                    inst_type = doc["_id"]["instrument_type"]
                    if strike is not None and inst_type in ('CE', 'PE'):
                        key = (name, int(strike), inst_type)
                        self.strike_cache[key] = {
                            "last_price": doc["last_price"],
                            "timestamp": doc["timestamp"],
                            "instrument_token": doc.get("instrument_token")
                        }
                self.cache_size = len(self.strike_cache)
                self.last_refresh_time = time_mod.time()

            if force:
                logging.info(f"Zerodha MongoDB cache refreshed: {self.cache_size} instruments loaded")

            return self.cache_size > 0 or force

        except Exception as e:
            logging.error(f"Error refreshing Zerodha MongoDB cache: {e}")
            return False

    def get_ltp_by_strike(self, symbol, strike, opt_type):
        with self.lock:
            data = self.strike_cache.get((symbol, int(strike), opt_type))
            if data:
                return data["last_price"]
        return None

    def get_cache_stats(self):
        with self.lock:
            return {
                'cache_size': self.cache_size,
                'last_refresh': datetime.fromtimestamp(self.last_refresh_time).strftime('%H:%M:%S') if self.last_refresh_time else 'Never',
                'snapshot_path': self.get_snapshot_path(),
                'last_modified': 'N/A (MongoDB)'
            }


def check_oi_signal_and_update():
    """
    Read OI signal from signals DB (generated by V1.3_oi_signal_generator) and update global OI_SIGNAL_STATE.

    Reads signal_unwinding table (Strat-1):
      net_oi_change = call_oi_change - put_oi_change
      If net_oi_change > 0 (bullish): More call OI building
      If net_oi_change < 0 (bearish): More put OI building

    Returns:
        str: 'BULLISH', 'BEARISH', or None
    """
    global OI_SIGNAL_STATE, LAST_OI_CHECK_TIME, OI_CURRENT_DATA

    if not ENABLE_OI_EXIT:
        return None

    try:
        today_str = datetime.now(IST).strftime('%Y%m%d')
        signals_db_path = os.path.join(SIGNALS_DB_DIR, f"signals_SENSEX_{today_str}.db")

        if not os.path.exists(signals_db_path):
            logging.warning(f"OI EXIT ENABLED but signals DB not found: {signals_db_path}")
            logging.warning("Ensure V1.3_oi_signal_generator is running with SYMBOL='SENSEX'. OI exits disabled until signals DB appears.")
            LAST_OI_CHECK_TIME = datetime.now(IST)
            return None

        # Open in read-only mode to prevent blocking the signal generator
        signals_db_uri = f"file:{signals_db_path}?mode=ro"
        conn = sqlite3.connect(signals_db_uri, uri=True, timeout=30.0)
        cursor = conn.cursor()
        cursor.execute("""
            SELECT signal_value, net_oi_change, call_oi_change, put_oi_change, timestamp
            FROM signal_unwinding
            ORDER BY id DESC LIMIT 1
        """)
        row = cursor.fetchone()
        conn.close()

        if not row:
            logging.warning("OI EXIT ENABLED but no signal data in signals DB yet (signal generator may still be starting)")
            LAST_OI_CHECK_TIME = datetime.now(IST)
            return None

        signal_value, net_oi_change, call_oi_change, put_oi_change, timestamp = row

        # Determine state from net_oi_change
        new_state = None
        if net_oi_change > 0:
            new_state = 'BULLISH'
        elif net_oi_change < 0:
            new_state = 'BEARISH'

        # Log OI values
        logging.info("=" * 60)
        if new_state != OI_SIGNAL_STATE:
            logging.info(f"OI SIGNAL CHANGED: {OI_SIGNAL_STATE} -> {new_state}")
        else:
            logging.info(f"OI SIGNAL UPDATE: {new_state if new_state else 'NEUTRAL'}")

        logging.info(f"  Signal Source:    {signals_db_path}")
        logging.info(f"  Signal Timestamp: {timestamp}")
        logging.info(f"  Call OI Change:   {call_oi_change:+,}")
        logging.info(f"  Put OI Change:    {put_oi_change:+,}")
        logging.info(f"  Net OI Change:    {net_oi_change:+,}")

        if new_state == 'BULLISH':
            logging.info("  Action: Block entries & force exit trades ABOVE ATM (LV+1, LV+2, LV+3)")
        elif new_state == 'BEARISH':
            logging.info("  Action: Block entries & force exit trades BELOW ATM (LV-1, LV-2, LV-3)")
        else:
            logging.info("  Action: No OI-based restrictions (signal near zero)")
        logging.info("=" * 60)

        # Store current OI data globally for logging in other functions
        OI_CURRENT_DATA = {
            'current_call_oi': call_oi_change,
            'current_put_oi': put_oi_change,
            'call_oi_change': call_oi_change,
            'put_oi_change': put_oi_change,
            'oi_signal_value': net_oi_change
        }

        OI_SIGNAL_STATE = new_state
        LAST_OI_CHECK_TIME = datetime.now(IST)

        return new_state

    except Exception as e:
        logging.error(f"Error checking OI signal: {e}", exc_info=True)
        return None


def should_oi_force_exit(level_name, oi_signal_state):
    """
    Determine if a trade at the given level should be force-exited based on OI signal.

    Logic:
    - BULLISH OI (more call buying): Exit trades ABOVE ATM (LV+1, LV+2, LV+3)
    - BEARISH OI (more put buying): Exit trades BELOW ATM (LV-1, LV-2, LV-3)
    """
    if not ENABLE_OI_EXIT or oi_signal_state is None:
        return False

    try:
        level_index = int(level_name.split('ATM_LV')[1])
    except (IndexError, ValueError):
        logging.warning(f"Could not parse level index from: {level_name}")
        return False

    # BULLISH signal: Exit positions ABOVE ATM (positive level indices)
    if oi_signal_state == 'BULLISH' and level_index > 0:
        return True

    # BEARISH signal: Exit positions BELOW ATM (negative level indices)
    if oi_signal_state == 'BEARISH' and level_index < 0:
        return True

    return False


